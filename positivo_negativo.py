# Programa que mostra se um numero é inteiro positivo ou negativo

numero = input(print("Entre com com o valor de um número:   "))

numero = float(numero)

if numero == 0:
    print("O número é nulo")
elif numero < 0:
    print("O Número é negativo")
else:
    print("Numero Postivo")

